def patch_suprep_standard_print_en(canonical: str) -> str:
    """Apply the personalization layer to suprep-standard-print.en.html."""
    out = canonical

    # 1. Strip legal-footer CSS + topbar-logo CSS + favicon/meta head block.
    #    The suprep canonical has these inlined (not as {{PARTIAL_FOOTER_LEGAL}}).
