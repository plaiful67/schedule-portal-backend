def main() -> int:
    failures: list[str] = []
    for vendor_skill, canonical_name, out_subdir, out_name, patch_fn in VARIANTS:
        canonical_path = VENDOR_ROOT / vendor_skill / "templates" / canonical_name
        out_dir = TEMPLATES_ROOT / out_subdir
        out_path = out_dir / out_name
        if not canonical_path.exists():
            failures.append(
                f"{vendor_skill}/{canonical_name}: vendor copy not found at "
                f"{canonical_path}. Run `make vendor-sync` first."
            )
            continue
        out_dir.mkdir(parents=True, exist_ok=True)
        try:
            personalized = patch_fn(canonical_path.read_text(encoding="utf-8"))
        except RuntimeError as e:
            failures.append(f"{vendor_skill}/{canonical_name}: {e}")
            continue
        out_path.write_text(personalized, encoding="utf-8")
        print(f"OK   {out_subdir}/{out_name}  ({len(personalized):,} bytes)")

    if failures:
        print("\nFAILURES:", file=sys.stderr)
        for f in failures:
            print(f"  - {f}", file=sys.stderr)
        return 1
    return 0

if __name__ == "__main__":
    sys.exit(main())
