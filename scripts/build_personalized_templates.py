#!/usr/bin/env python3
"""Generate the schedule-portal personalized print templates from the canonical
bowel-prep skill templates.

Why: The portal needs the same design as the canonical static handouts at
egdcolon.giready.com — yellow boxes, page-break cadence, MiraLAX wording — plus
a personalization layer (one-row appointment callout, pz-only heading suffixes,
pz-only inline date phrases) baked in for server-side WeasyPrint rendering.

Maintaining two parallel HTML templates by hand caused drift. This script makes
the personalized variants derived build artifacts of the canonical, so a fix in
~/.claude/skills/bowel-prep-generator/templates/ flows to the portal on the next
`make vendor-sync`.

Add new variants by writing a new patch function and wiring it in `main()`.
"""
from __future__ import annotations

import sys
from pathlib import Path

BACKEND_DIR = Path(__file__).resolve().parent.parent
VENDOR_ROOT = BACKEND_DIR / "vendor"            # vendor/<skill>/templates/<name>
TEMPLATES_ROOT = BACKEND_DIR / "app" / "templates"  # app/templates/<out_subdir>/<name>

BANNER = (
    "<!-- GENERATED by scripts/build_personalized_templates.py — DO NOT HAND-EDIT.\n"
    "     Edit the canonical template at\n"
    "     ~/.claude/skills/bowel-prep-generator/templates/<name>.html\n"
    "     and/or this script's patch function, then re-run `make vendor-sync`. -->\n"
)


def _replace_unique(text: str, old: str, new: str, *, where: str) -> str:
    """Replace `old` with `new` in `text`. Raises if `old` doesn't appear
    exactly once — that way a stale patch fails loudly the next time the
    canonical template moves.
    """
    count = text.count(old)
    if count != 1:
        raise RuntimeError(
            f"patch '{where}' expected exactly 1 match for old string, got {count}.\n"
            f"old: {old!r}\n"
        )
    return text.replace(old, new, 1)


