# -----------------------------------------------------------------------------
# Entry point.
# -----------------------------------------------------------------------------

# (canonical filename, output filename, patch function)
VARIANTS = [
    ("combined-print.en.html", "combined-print-personalized.en.html", patch_combined_print_en),
    ("combined-print.es.html", "combined-print-personalized.es.html", patch_combined_print_es),
]


def main() -> int:
    if not VENDOR_TEMPLATES.exists():
        print(f"FATAL: vendor templates not found at {VENDOR_TEMPLATES}.\n"
              f"Run `make vendor-sync` first.", file=sys.stderr)
        return 1
    OUT_DIR.mkdir(parents=True, exist_ok=True)

    failures: list[str] = []
    for canonical_name, out_name, patch_fn in VARIANTS:
        canonical_path = VENDOR_TEMPLATES / canonical_name
        out_path = OUT_DIR / out_name
        if not canonical_path.exists():
            failures.append(f"{canonical_name}: not found at {canonical_path}")
            continue
        try:
            personalized = patch_fn(canonical_path.read_text(encoding="utf-8"))
        except RuntimeError as e:
            failures.append(f"{canonical_name}: {e}")
            continue
        out_path.write_text(personalized, encoding="utf-8")
        print(f"OK   {out_name}  ({len(personalized):,} bytes)")

    if failures:
        print("\nFAILURES:", file=sys.stderr)
        for f in failures:
            print(f"  - {f}", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main())
