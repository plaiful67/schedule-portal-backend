def _strip_legal_footer(text: str, *, lang: str) -> str:
    text = _replace_unique(text, _LEGAL_FOOTER_CSS, "", where=f"strip legal-footer CSS ({lang})")
    html = _LEGAL_FOOTER_HTML_EN if lang == "en" else _LEGAL_FOOTER_HTML_ES
    text = _replace_unique(text, html, "", where=f"strip legal-footer HTML ({lang})")
    return text


def patch_combined_print_en(canonical: str) -> str:
    """Apply the personalization layer to the canonical combined-print.en.html."""
    out = canonical

    # 0. Strip the privacy/terms/disclaimer footer (personalized-only).
    out = _strip_legal_footer(out, lang="en")

    # 1. CSS for appt-callout + performing-physician + followup-callout
    #    (appended pre-</style>).
    out = _inject_personalization_css(out)

    # 1b. Performing-physician callout under the band-label div.
    out = _replace_unique(
        out,
        '<div class="band-label">{{BAND_LABEL}}</div>',
        '<div class="band-label">{{BAND_LABEL}}</div>\n'
        + _PERFORMING_PHYSICIAN_HTML_EN,
        where="combined en: performing-physician callout under band-label",
    )

    # 2. Insert appt-callout section between cover </section> and the LOCATION
    #    section. Anchor on the LOCATION marker comment.
    out = _replace_unique(
        out,
        "<!-- LOCATION -->",
        _APPT_CALLOUT_HTML_EN + "<!-- LOCATION (under the appointment callout) -->",
        where="combined en: appt-callout HTML insertion",
    )

    # 3. Inject the followup placeholder just before the "WHAT ARE THESE
    #    PROCEDURES" section header. We use the multi-line section comment
    #    block as the anchor so we land in the right slot even if the
    #    location section gains internal markup.
    out = _replace_unique(
        out,
        "<!-- ============================================================= -->\n<!-- WHAT ARE THESE PROCEDURES (combined-handout intro)           -->",
        "<!-- Follow-up appointment (narrow standalone callout, personalized) -->\n{{FOLLOWUP_BLOCK_HTML}}\n\n<!-- ============================================================= -->\n<!-- WHAT ARE THESE PROCEDURES (combined-handout intro)           -->",
        where="combined en: followup token before procedures section",
    )

    # 4. Bowel-Prep paragraph: wrap "the day before" inline.
    out = _replace_unique(
        out,
        "The medicines below empty it over the day before.",
        'The medicines below empty it over <span class="pz-only" data-pz-day="-1" data-pz-template="the day before ({date})">the day before</span>.',
        where="combined en: bowel prep 'day before' wrap",
    )

    # 5. Step headings — add data-pz-day + data-pz-suffix attributes.
    out = _replace_unique(
        out,
        '<h2 class="step"><span class="icon">&#128197;</span> 3 Days Before &mdash; Low-Residue Diet</h2>',
        '<h2 class="step" data-pz-day="-3" data-pz-suffix=" &mdash; Low-Residue Diet"><span class="icon">&#128197;</span> 3 Days Before &mdash; Low-Residue Diet</h2>',
        where="combined en: 3-days-before heading",
    )
    out = _replace_unique(
        out,
        '<h2 class="step"><span class="icon">&#128197;</span> 1 Day Before &mdash; Low-Residue Through Lunch</h2>',
        '<h2 class="step" data-pz-day="-1" data-pz-suffix=" &mdash; Low-Residue Through Lunch"><span class="icon">&#128197;</span> 1 Day Before &mdash; Low-Residue Through Lunch</h2>',
        where="combined en: 1-day-before heading",
    )
    out = _replace_unique(
        out,
        '<h2 class="step"><span class="icon">&#128197;</span> Day of Procedure</h2>',
        '<h2 class="step" data-pz-day="0" data-pz-suffix=" &mdash; Day of Procedure"><span class="icon">&#128197;</span> Day of Procedure</h2>',
        where="combined en: day-of heading",
    )

    # 6. Day-of "Clear liquids only after 2 PM the day before."
    out = _replace_unique(
        out,
        "<strong>Clear liquids only after 2 PM the day before.</strong>",
        '<strong>Clear liquids only after 2 PM <span class="pz-only" data-pz-day="-1" data-pz-template="on {date}">the day before</span>.</strong>',
        where="combined en: clear-liquids inline",
    )

    # 7. Meals section: wrap 3 inline phrases.
    out = _replace_unique(
        out,
        "Continue this diet for the 3 days before the procedure, through lunch the day before. After 2:00 PM the day before, switch to clear liquids only",
        'Continue this diet for <span class="pz-only" data-pz-day="-3" data-pz-template="the 3 days before the procedure (starting {date})">the 3 days before the procedure</span>, through lunch <span class="pz-only" data-pz-day="-1" data-pz-template="the day before ({date})">the day before</span>. After 2:00 PM <span class="pz-only" data-pz-day="-1" data-pz-template="on {date}">the day before</span>, switch to clear liquids only',
        where="combined en: meals inline pz spans",
    )

    return BANNER + out


